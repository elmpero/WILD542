#Appendix B. OpenBUGS code and data from Armstrong et al 2017

#Supplementary data B.1. OpenBUGS code for estimating the strength and duration of post-release effects on survival of translocated animals. The code below allows the strength of post-release effects to differ for males and females, and assumes sexes are known.

#Model
{
  # Priors for survival analysis
  a.phi ~ dnorm(0, 0.01)		# logit normal annual female survival probability
  b.r ~ dnorm(0, 0.01)		# post-release effect on logit survival probability
  b.s ~ dnorm(0, 0.01)		# sex effect on logit survival probability
  b.rs ~ dnorm(0, 0.01)		# interaction between sex and post-release effects
  T.max <- sum(interval[])	# calculate duration of study
  T ~ dunif(0, T.max)		# acclimation period after release
  a.p ~ dnorm(0, 0.01)		# logit mean re-sighting probability
  s.p.t ~ dunif(0, 10)		# sd in logit(p) over time
  tau.p.t <- pow(s.p.t, -2)	# precision in logit(p) over time
  
  # Survival model
  t[1] <- 0	# time = 0 at release
  for (j in 2:surveys) {
    t[j] <- t[j-1]+interval[j-1]		# calculate time at end of each survey
    re.p.t[j] ~ dnorm(0, tau.p.t)		# assign random time effect on logit(p)
    logit(p[j]) <- a.p+re.p.t[j]		# calculate p for each survey
  }
  
  for (i in 1:n)  {				# for each individual...
    alive[i, first[i]] <- 1 			# 1 = alive the first time recorded (at release in this case)
    for (j in first[i]+1:surveys)  {		# for each survey...
      y[i, j] ~ dbern(psight[i, j])						# sample encounter based on probability of being seen
      psight[i, j] <- p[j]*alive[i, j]    					# probability of being seen is p multiplied by whether alive
      alive[i, j] ~ dbern(palive[i, j])					# sample whether individual alive
      palive[i, j] <- phi.interval[i, j]*alive[i, j-1]			# calculate probability that individual is alive
      phi.interval[i, j] <- pow(phi[i, j], interval[j -1])			# calculate probability of surviving interval if alive at start
      logit(phi2[i,j]) <- a.phi+b.s*sex[i]					# annual survival probability if after T
      logit(phi1[i, j]) <- logit(phi2[i,j])+trans[i]*(b.r+b.rs*sex[i])	# annual survival probability if before T
      phi[i,j] <-
        phi1[i,j]*step(T-(t[j]-t[first[i]]))+
        pow(phi1[i,j],T-(t[j-1]-t[first[i]]))*pow(phi2[i,j],(t[j]-t[first[i]])-T)*step(T-(t[j-1]-t[first[i]]))*(1-step(T-(t[j]-t[first[i]])))+
        phi2[i,j]*(1-step(T-(t[j-1]-t[first[i]])))
      # 1st line applies if whole interval is before T
      # 2nd line applies if part of interval before T and part of interval after T
      # 3rd line applies if whole interval is after T
    }
  }
  
  # Calculations
  logit(phi2.fem) <- a.phi	# normal annual female survival probability
  logit(phi1.fem) <- a.phi+b.r	# annual female survival probability during acclimation period
  logit(phi2.male) <- a.phi+b.s	# normal annual male survival probability
  logit(phi1.male) <- a.phi+b.s+b.r+b.rs	# annual male survival probability during acclimation period
  COR.fem <- 1-pow(phi1.fem/phi2.fem,T)	# Cost of Release for females
  COR.male <- 1-pow(phi1.male/phi2.male,T) 	# Cost of Release for males
}


